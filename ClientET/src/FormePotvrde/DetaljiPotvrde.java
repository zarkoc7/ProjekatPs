    private void popunisve() {
        jTable1.setModel(new TableModelTrotinet());
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        try {
            ArrayList<StavkaCenovnika> stavke;
            ArrayList<StavkaCenovnika> sveStavke;
            ArrayList<Cenovnik> cenovnici = ClientKontroler.getInstance().getAllCenovnik();
            for (Cenovnik cenovnik : cenovnici) {

                stavke = ClientKontroler.getInstance().getAllStavke(cenovnik);
                for (StavkaCenovnika stavka : stavke) {
                    jComboBox1.addItem(stavka);
                    pomocna.add(stavka);

                }
            }
            ArrayList<Klijent> klijenti = ClientKontroler.getInstance().getAllKlijent();
            for (Klijent klijent : klijenti) {
                jComboBox2.addItem(klijent);

            }

            TableModelTrotinet tt = (TableModelTrotinet) jTable1.getModel();

        } catch (Exception ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

