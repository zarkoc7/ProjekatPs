    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date DatumOd = sdf.parse(txtDatumP.getText());
            Date DatumDo = sdf.parse(txtDatumZ.getText());

            long millisecondsPerDay = 24 * 60 * 60 * 1000; // Number of milliseconds in a day
            long razlikaIzmedjuDana = DatumDo.getTime() - DatumOd.getTime();
            long razlika = razlikaIzmedjuDana / millisecondsPerDay;
            StavkaCenovnika sta = (StavkaCenovnika) jComboBox1.getSelectedItem();

            double cena = sta.getCena() * razlika;
            jTextField2.setText(String.valueOf(cena));
        } catch (ParseException ex) {
            Logger.getLogger(DetaljiPotvrde.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (tr == null) {
            JOptionPane.showMessageDialog(this, "izaberi trotinet");
        }

        if (txtDatumP.getText().isEmpty() || txtDatumZ.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "popunite sva polja");
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");

        try {
            Date datumOd = sdf.parse(txtDatumP.getText());
            Date datumdo = sdf.parse(txtDatumZ.getText());

            if (datumOd.before(new Date())) {
                JOptionPane.showMessageDialog(this, "Datum mora da bude posle danasnjeg");
                return;
            }

            if (!datumOd.before(datumdo)) {
                JOptionPane.showMessageDialog(this, "DatumDo ne sme biti pre DatumaOd");
                return;
            }

            double cena = Double.parseDouble(jTextField2.getText());
            Klijent k = (Klijent) jComboBox2.getSelectedItem();
            StavkaCenovnika st = (StavkaCenovnika) jComboBox1.getSelectedItem();
            Potvrda potvrda = new Potvrda(izabrana.getPotvrdaId(), datumOd, datumdo, cena, tr, k, st, izabrana.getProdavac(), st.getCenovnik());
            try {
                ClientKontroler.getInstance().UpdatePotvrda(potvrda);
                JOptionPane.showMessageDialog(this, "Potvrda je izmenjena");
                KlijentskaForma kf = (KlijentskaForma) getParent();
                kf.osveziTabelu();
                this.dispose();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
//                Logger.getLogger(DodajPotvrduForm.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (ParseException ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

