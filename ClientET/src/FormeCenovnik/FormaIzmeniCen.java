    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtCena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Popunite polja");
            return;
        }
        
        double price = Double.parseDouble(txtCena.getText());
        Valuta val = (Valuta) jComboBox1.getSelectedItem();
        TipObracuna tip = (TipObracuna) jComboBox2.getSelectedItem();
        TableModelStavka tm = (TableModelStavka) jTable1.getModel();
        StavkaCenovnika stavka = new StavkaCenovnika(-1l, cenovnik, price, val, tip);
        tm.dodajStavku(stavka);
        nove.add(stavka);
        brojac++;
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {

            // TODO add your handling code here:
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            if (txtDatumP.getText().isEmpty() || txtDatumKraj.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Datumi nisu uneseni");
                return;
            }
            Date datumOd = sdf.parse(txtDatumP.getText());
            Date datumDo = sdf.parse(txtDatumKraj.getText());

            if(!datumOd.equals(cenovnik.getVaziOd())){
            if (new Date().after(datumOd)) {
                JOptionPane.showMessageDialog(this, "Unesite datum od posle danasnjeg dana!");
                return;
            }}
            
            if (datumOd.after(datumDo)) {
                JOptionPane.showMessageDialog(this, "Greska, datumi zavrsetka ne moze biti pre pocetka");
                return;
            }

            TableModelStavka tm = (TableModelStavka) jTable1.getModel();
            ArrayList<StavkaCenovnika> lista = tm.vratiListu();
//            if(brojac>0){
//                for(int j=0;j<brojac;j++){
//                lista.get(lista.size()-j);
//                ClientKontroler.getInstance().addStavkaCen(cenovnik.getCenovnikId());
//                
//                }
//            
//            
//            }

            

            cenovnik.setVaziOd(datumOd);
            cenovnik.setVaziDo(datumDo);
            cenovnik.setProdavac(k);
            cenovnik.setStavkeC(lista);

            try {
                ClientKontroler.getInstance().UpdateCenovnik(cenovnik);
                JOptionPane.showMessageDialog(this, "Cenovnik je izmenjen");
                FormaPretragaCen fc=(FormaPretragaCen) getParent();
                fc.reakcija();
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());

            }

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        
        try {
            int row = jTable1.getSelectedRow();
            
            
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "nijedan red nije izabran");
                return;
            }
            
            TableModelStavka mt = (TableModelStavka) jTable1.getModel();
            StavkaCenovnika sc=mt.vratiStavkuCen(row);
            ArrayList<Potvrda> potvrde= ClientKontroler.getInstance().getAllPotvrde();
            for (Potvrda potvrda : potvrde) {
                if(potvrda.getStavkaCenovnika().getStavkaCenId().equals(sc.getStavkaCenId())&&potvrda.getCenovnik().getCenovnikId().equals(cenovnik.getCenovnikId()))
                {
                    JOptionPane.showMessageDialog(this, "Stavka cenovnika ne moze biti obrisana zato sto je u upotrebi");
                    return;
                }
            }
            mt.obrisiRed(row);
        } catch (Exception ex) {
            Logger.getLogger(FormaIzmeniCen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

