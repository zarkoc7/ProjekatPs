    private void popunicb() {
     jComboBox1.removeAllItems();
     jComboBox2.removeAllItems();

     for (Valuta val : Valuta.values()) {
         jComboBox1.addItem(val);
     }

     for (TipObracuna tip : TipObracuna.values()) {
         jComboBox2.addItem(tip);
     }

    }
